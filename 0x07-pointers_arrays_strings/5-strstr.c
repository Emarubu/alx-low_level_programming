#include "main.h"
#include <string.h>
/**
* _strstr - locates a substring
* @haystack: first string
* @needle: second
* Return: a pointer or a Null
*/
char *_strstr(char *haystack, char *needle)
{
	int i;
	int k;

	while (*haystack != '\0')
	{
	if (*needle != '\0')
	haystack[i] == needle[i];
	return (needle);
	}
		i++;
		k++;
		return (NULL);
}



